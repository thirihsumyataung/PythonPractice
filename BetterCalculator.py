
input1 = float(input("Enter First Number: "))
op = input("Enter OPERATOR: ")
input2 = float(input("Enter First Number: "))

if op == "*" :
    print(input1 * input2)
elif op == "+":
        print(input1 + input2)
elif op == "-":
        print(input1 - input2)
elif op == "/":
        print(input1 / input2)
else:
    print("\n\n\n---Invalid Operator---")